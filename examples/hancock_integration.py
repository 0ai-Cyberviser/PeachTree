def load_peachtree_dataset(dataset_path: str) -> List[Dict]:
    """Load PeachTree dataset in ChatML format"""
    dataset = []
    with open(dataset_path, 'r', encoding='utf-8') as f:
        for line in f:
            if line.strip():
                dataset.append(json.loads(line))
    return dataset


def validate_dataset_quality(dataset: List[Dict]) -> Dict:
    """Validate dataset meets quality requirements for Hancock training"""
    results = {
        "total_records": len(dataset),
        "has_system_prompt": 0,
        "has_user_query": 0,
        "has_assistant_response": 0,
        "avg_conversation_length": 0,
        "security_keywords_found": 0,
    }
    
    security_keywords = {
        "vulnerability", "exploit", "CVE", "security", "malware",
        "attack", "defense", "threat", "authentication", "encryption"
    }
    
    total_length = 0
    for record in dataset:
        messages = record.get("messages", [])
        
        # Check message structure
        for msg in messages:
            if msg.get("role") == "system":
                results["has_system_prompt"] += 1
            elif msg.get("role") == "user":
                results["has_user_query"] += 1
            elif msg.get("role") == "assistant":
                results["has_assistant_response"] += 1
            
            # Check for security content
            content = msg.get("content", "").lower()
            if any(keyword in content for keyword in security_keywords):
                results["security_keywords_found"] += 1
                break
        
        total_length += len(messages)
    
    if dataset:
        results["avg_conversation_length"] = total_length / len(dataset)
    
    return results


